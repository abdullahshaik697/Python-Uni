# List of like counts for several posts
like_counts = [120, 45, 300, 75, 500, 200]

# Sort the list in ascending order (optional)
sorted_likes = sorted(like_counts)

# Find the post with the highest number of likes
highest_likes = max(like_counts)

# Print the results
print("Sorted Like Counts:", sorted_likes)
print("Highest Like Count:", highest_likes)